package java16.enums;

/**
 * Менюдогу тамактардын вегетариандык абалын сүрөттөйт.
 * VEGETARIAN - Тамак толугу менен вегетариандык (эт же жаныбар азыктары жок).
 * NON_VEGETARIAN - Тамакта эт же жаныбардан алынган азыктар бар.
 * UNKNOWN - Тамактын вегетариандык абалы белгисиз же такталбаган.
 */
public enum VegetarianStatus {
    VEGETARIAN,
    NON_VEGETARIAN,
    UNKNOWN
}